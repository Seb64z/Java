    void mostrar(trabajador x){
        imprimir("Codigo: "+x.codigo);
        imprimir("Nombre: "+x.nombre);
        imprimir("Tarifa: "+x.tarifa);
        imprimir("Horas trabajadas: "+x.horasT);
        imprimir("Sueldo Bruto: "+x.calcularBruto());
        imprimir("Descuento por Esalud: "+x.calcularSALUD());
        imprimir("Descuento por AFP: "+x.calcularAFP());
        imprimir("Descuento total: "+x.calcularDescuento());
        imprimir("Sueldo Neto: "+x.calcularNeto());
    }
    void imprimir(String texto){
        txtSalida.append(texto+"\n");
    }
